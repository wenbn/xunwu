package www.ucforward.com.dto;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

/**
 * 订单导出excel
 * @Auther: lsq
 * @Date: 2018/9/28 16:07
 * @Description:
 */
public class OrderExport implements Serializable {
    /**
     * 订单编号
     */
    private String orderCode;
    /**
     * 订单金额
     */
    private BigDecimal orderAmount;
    /**
     * 订单状态 0:待付款 1:已支付 2:财务已审核（待确认正式合同）3:已确认正式合同 4:线下合同派单 5:已签署合同 6:确认Ejari合同签订(出租才有,订单结束) 7:卖家NOC /买家贷款（出售） 8:完成产权变更 9:已完成 10:买家拒绝签正式合同 11:业主拒绝签正式合同 12:账务审核不通过 13:申请退款 14:已退款
     */
    private String orderStatus;
    /**
     * 支付方式 0-未付款 1-线上支付 2-线下支付 3-钱包支付
     */
    private String payWay;
    /**
     * 支付状态 0-未付款 1- 已支付
     */
    private String payStatus;
    /**
     * 交易状态 0:交易中 1:交易成功 2:交易失败
     */
    private String tradingStatus;
    /**
     * 支付时间
     */
    private Date payTime;
    /**
     * 订单类型 0-租房->1-买房
     */
    private String orderType;
    /**
     * 房源名称
     */
    private String houseName;
    /**
     * 房源编号
     */
    private String houseCode;
    /**
     * 城市
     */
    private String city;
    /**
     * 社区
     */
    private String community;
    /**
     * 子社区
     */
    private String subCommunity;
    /**
     * 房源所在区域名称
     */
    private String address;

    public String getOrderCode() {
        return orderCode;
    }

    public void setOrderCode(String orderCode) {
        this.orderCode = orderCode;
    }

    public BigDecimal getOrderAmount() {
        return orderAmount;
    }

    public void setOrderAmount(BigDecimal orderAmount) {
        this.orderAmount = orderAmount;
    }

    public String getOrderStatus() {
        return orderStatus;
    }

    public void setOrderStatus(String orderStatus) {
        this.orderStatus = orderStatus;
    }

    public String getPayWay() {
        return payWay;
    }

    public void setPayWay(String payWay) {
        this.payWay = payWay;
    }

    public String getPayStatus() {
        return payStatus;
    }

    public void setPayStatus(String payStatus) {
        this.payStatus = payStatus;
    }

    public String getTradingStatus() {
        return tradingStatus;
    }

    public void setTradingStatus(String tradingStatus) {
        this.tradingStatus = tradingStatus;
    }

    public Date getPayTime() {
        return payTime;
    }

    public void setPayTime(Date payTime) {
        this.payTime = payTime;
    }

    public String getOrderType() {
        return orderType;
    }

    public void setOrderType(String orderType) {
        this.orderType = orderType;
    }

    public String getHouseName() {
        return houseName;
    }

    public void setHouseName(String houseName) {
        this.houseName = houseName;
    }

    public String getHouseCode() {
        return houseCode;
    }

    public void setHouseCode(String houseCode) {
        this.houseCode = houseCode;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCommunity() {
        return community;
    }

    public void setCommunity(String community) {
        this.community = community;
    }

    public String getSubCommunity() {
        return subCommunity;
    }

    public void setSubCommunity(String subCommunity) {
        this.subCommunity = subCommunity;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
