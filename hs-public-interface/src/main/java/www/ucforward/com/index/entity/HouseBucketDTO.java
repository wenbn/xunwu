package www.ucforward.com.index.entity;

import java.io.Serializable;

/**
 * @author wenbn
 * @version 1.0
 * @date 2018/8/16
 */
public class HouseBucketDTO implements Serializable{

    /**
     * 聚合bucket的key
     */
    private String key;

    /**
     * 聚合结果值
     */
    private long count;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public long getCount() {
        return count;
    }

    public void setCount(long count) {
        this.count = count;
    }

    public HouseBucketDTO(String key, long count) {
        this.key = key;
        this.count = count;
    }

    public HouseBucketDTO() {
    }
}
